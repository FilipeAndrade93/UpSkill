package Autonomo3;

import java.util.Arrays;

public class Autonomo3 {

    public static void main(String[] args) {
        int[] array = {1, 2, 3};

        System.out.println(Arrays.toString(inverse(array)));
    }

    public static int[] inverse(int[] a){

        for (int i = a.length - 1; i >= 0; i--){
            int b = a[i];
        }
        b;
    }
}
