package Autonomo3;

public class Calculator {

    public Calculator(){
    }

    public int sum(int a, int b){
        return a + b;
    }

    public int sub(int a, int b){
        return a - b;
    }

    public double division(double a, double b){
        return a / b;
    }

    public int multiplication(int a, int b){
        return a * b;
    }
}
